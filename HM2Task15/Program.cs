Console.Write("Введите цифровое обозначение дня недели: ");
int day = int.Parse(Console.ReadLine()??"0");
//Используем ключ
if(dayOfWeek.ContainsKey(day))
{
    Console.WriteLine(dayOfWeek[day]); 
}
else
{
    Console.WriteLine("Это не день недели!");
