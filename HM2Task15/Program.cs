Dictionary<int, string> dayOfWeek = new Dictionary<int, string>();
dayOfWeek.Add(1, "Понедельник, придётся поработать");
dayOfWeek.Add(2, "Вторник, понедельник пережили, худшее позади!");
dayOfWeek.Add(3, "Cреда - маленькая пятница, осталось немного!");
